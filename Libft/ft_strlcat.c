#include "libft.h"

size_t	ft_strlen(const char *s);

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	d_len;
	size_t	s_len;
	size_t	i;
	size_t	j;

	d_len = 0;
	while (dst[d_len] && d_len < size)
		d_len++;
	s_len = ft_strlen(src);
	if (d_len >= size)
		return (size + s_len);
	i = d_len;
	j = 0;
	while (src[j] && i < (size - 1))
	{
		dst[i] = src[j];
		i++;
		j++;
	}
	dst[i] = '\0';
	return (d_len + s_len);
}
